/**
 * Resolves the list of parts for a lesson.
 * Falls back to converting keyConcepts into parts if no curated list exists.
 */
export const getLessonParts = (lessonId, lessonContent) => {
  if (lessonParts[lessonId]) {
    return lessonParts[lessonId];
  }

  // Fallback
  const concepts = lessonContent?.keyConcepts || [];
  return concepts.map((concept, index) => ({
    id: `concept-${index}`,
    name: concept.name,
    desc: concept.desc,
    fact: `Key concept essential to understanding ${concept.name}.`
  }));
};
